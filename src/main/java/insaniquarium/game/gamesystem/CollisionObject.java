package insaniquarium.game.gamesystem;

public class CollisionObject {

    public CollisionObject(int id, float x, float y, int radius){
        this.id = id;
        this.x = x;
        this.y = y;
        this.radius = radius;

    }
    public int id;
    public float x;
    public float y;
    public int radius;



}
